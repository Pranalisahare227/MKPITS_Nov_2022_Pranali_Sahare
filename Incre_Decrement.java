//Incre/Decrement Operator

class Incre_Decrement {

  public static void main(String[] args){

    int a=11;
    System.out.println(a);
    System.out.println("Prepost Increment");
    System.out.println(a++);
    System.out.println(++a);

    System.out.println("Prepost Decrement");
	System.out.println(a--);
	System.out.println(--a);

  }

}